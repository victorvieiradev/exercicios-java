package catalisa.lista03b;

public class ContandoAte100 {
    public static void main(String[] args) {
        int c = 1;
        while ( c <= 100){
            System.out.println(c);
            c++;
        }
        for (int i = 1; i <= 100; i++){
            System.out.println(i);
        }
    }
}
